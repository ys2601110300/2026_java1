package ai0416;

public class aiIfElseTest {
    public static void main(String[] args) {
        int num = 200;

        if(num < 100) {
            System.out.println("조건식의 결과가 참.");
            System.out.println("100보다 작다.");
        }
            else {
            System.out.println("조건식의 결과가 거짓.");
            System.out.println("100보다 크다.");
        }

        System.out.println("프로그램 출력 끝!");
        }
}
